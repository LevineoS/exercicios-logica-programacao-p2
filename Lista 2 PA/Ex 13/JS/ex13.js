var dia = parseInt(prompt("De 1 a 7, escolha o dia da semana: "));

if (dia == 1){;
    console.log("1 - Domingo");
    alert("1 - Domingo");

}else if(dia == 2){;
    console.log("2 - Segunda-feira");
    alert("2 - Segunda");

}else if(dia == 3){;
    console.log("3 - Terça-feira");
    alert("3 - Terça-feira");

}else if(dia == 4){;
    console.log("4 - Quarta-feira");
    alert("4 - Quarta-feira");

}else if(dia == 5){;
    console.log("5 - Quinta-feira");
    alert("5 - Quinta-feira");

}else if(dia == 6){;
    console.log("6 - Sexta-feira");
    alert("6 - Sexta-feira");

}else if(dia == 7){;
    console.log("7 - Sábado");
    alert("7 - Sábado");

}else{
    console.log("Valor inválido!");
    alert("Valor inválido!");
}